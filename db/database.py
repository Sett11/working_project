# log_file —É–∫–∞–∑—ã–≤–∞–µ—Ç—Å—è –±–µ–∑ –ø–∞–ø–∫–∏ logs, —á—Ç–æ–±—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –¥–µ—Ñ–æ–ª—Ç–Ω—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ª–æ–≥–æ–≤.
